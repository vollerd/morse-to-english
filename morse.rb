def morse_to_eng(morse)
	output = ""
	incode = morse.split(/\s{1,2}/)
	codekey = { ".-"  => "A", "-."   => "N",
					"-..." => "B", "---"  => "O",
					"-.-." => "C", ".--." => "P",
					"-.."  => "D", "--.-" => "Q",
					"."    => "E", ".-."  => "R",
					"..-." => "F", "..."  => "S",
					"--."  => "G", "-"    => "T",
					"...." => "H", "..-"  => "U",
					".."   => "I", "...-" => "V",
					".---" => "J", ".--"  => "W",
					"-.-"  => "K", "-..-" => "X",
					".-.." => "L", "-.--" => "Y",
					"--"   => "M", "--.." => "Z",
					""     => " " }
					
	incode.each { |i|	output << codekey[i] }
	output
end 

puts morse_to_eng('.... . .-.. .--.   -- .   --- ..- -   --- ..-.   - .... .. ...   .-- . .-.. .-..')

